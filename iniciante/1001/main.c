#include <stdio.h>
 
int main() {
    // introduzindo os valores por meio de variaveis
    int A, B, X;
    
    // entrando os valores com scanf
    scanf("%d %d", &A, &B);
    
    // soma na variavel X
    X = A + B;
    
    // so fazer oque pede na saida
    printf("X = %d\n", X);
    
    return 0;
}